// Custom holidays storage
let customHolidays = JSON.parse(localStorage.getItem('customHolidays') || '[]');

// Function to get all holidays for a year
function getAllHolidays(year) {
  const nationalHolidays = KOREAN_HOLIDAYS[year] || [];
  const custom = customHolidays.filter((h) => h.date.startsWith(year.toString()));
  return [...nationalHolidays, ...custom];
}

// Function to add custom holiday
function addCustomHoliday(date, name, type = 'company') {
  const holiday = { date, name, type };
  customHolidays.push(holiday);
  localStorage.setItem('customHolidays', JSON.stringify(customHolidays));
  return holiday;
}

// Function to remove custom holiday
function removeCustomHoliday(date) {
  customHolidays = customHolidays.filter((h) => h.date !== date);
  localStorage.setItem('customHolidays', JSON.stringify(customHolidays));
}
